#pragma once

#include <stdint.h>
#include <stddef.h>

/* ── Positions / Ranges ──────────────────────────────────────────────────── */

typedef struct { uint32_t line, character; } LspPos;
typedef struct { LspPos start, end; } LspRange;

/* ── Token ───────────────────────────────────────────────────────────────── *
 *
 * TK_* constants are defined in grammar.tab.h (generated by bison) as the
 * yytokentype enum.  Files that compare token kinds must include grammar.tab.h
 * in addition to this header.
 *
 * TK_EOF is bison's EOF marker (always 0) and is not declared in grammar.y.
 */
#define TK_EOF 0

typedef struct {
    int    kind;  /* one of the TK_* / KW_* values from grammar.tab.h */
    LspPos start, end;
    char  *text;  /* heap-allocated; caller must free */
} Token;

void token_free(Token *t);

/* ── DocSymbol kinds (LSP SymbolKind values) ─────────────────────────────── */

#define SK_MODULE   2
#define SK_FUNCTION 12
#define SK_VARIABLE 13
#define SK_OBJECT   19
#define SK_EVENT    24

/* ── DocSymbol ───────────────────────────────────────────────────────────── *
 *
 * A named declaration (project, task, resource, account, shift) in the
 * document's symbol tree.  Mirrors LSP DocumentSymbol.  Used for
 * textDocument/documentSymbol, completion identifiers, and dep validation.
 */
typedef struct DocSymbol DocSymbol;
struct DocSymbol {
    char      *name;           /* display name, heap-allocated */
    char      *detail;         /* TJP identifier, heap-allocated */
    int        kind;           /* SK_* constant */
    LspRange   range;
    LspRange   selection_range;
    DocSymbol *children;       /* dynamic array */
    int        num_children;
    int        children_cap;
};

/* ── DepRef ──────────────────────────────────────────────────────────────── *
 *
 * A raw dependency reference captured during parsing.  Stored in ParseResult
 * for cross-file revalidation by diagnostics.c:revalidate_dep_refs().
 */
typedef struct {
    int       bang_count;
    char    **segs;       /* dot-split path segments, heap-allocated */
    int       nseg;
    char    **scope;      /* task scope snapshot at parse time, heap-allocated */
    int       scope_n;
    LspRange  range;
} DepRef;

/* ── Diagnostic severity ─────────────────────────────────────────────────── */

#define DIAG_ERROR   1
#define DIAG_WARNING 2

/* ── Diagnostic ──────────────────────────────────────────────────────────── */

typedef struct {
    LspRange range;
    int       severity;
    char     *message; /* heap-allocated */
} Diagnostic;

/* ── TokenSpan ───────────────────────────────────────────────────────────── *
 *
 * A lexical token with its source location.  Used as a flat, ordered sequence
 * for all cursor-position queries: hover, completion, signature help, and
 * semantic highlighting.  (Distinct from LSP "semantic tokens", which is a
 * separate highlighting protocol.)
 *
 * Populated by the lexer for every token that callers may need to inspect:
 *   - All KW_* keyword tokens (including TK_DATE, TK_DURATION, TK_STR, …)
 *   - TK_IDENT, TK_LBRACE, TK_RBRACE, TK_BANG, TK_DOT, TK_COMMA
 *   - TK_LINE_COMMENT, TK_BLOCK_COMMENT
 *
 * token_kind holds the raw TK_* / KW_* constant from grammar.tab.h.
 * text is heap-allocated (strdup of yytext); NULL only for tokens where
 * the text is never needed (e.g. comments).  Caller must not modify it;
 * parse_result_free() frees it.
 */
typedef struct {
    int    token_kind;
    LspPos start, end;
    char  *text;   /* heap-allocated; may be NULL */
} TokenSpan;

/* ── DefinitionLink ──────────────────────────────────────────────────────── *
 *
 * A resolved reference in the document.  source is the range of the
 * reference expression (e.g. a dependency path), target is the
 * selection_range of the symbol being referred to.
 *
 * Populated by validate_dep_refs() for every successfully resolved
 * dependency reference so that textDocument/definition can answer without
 * re-parsing.
 *
 * target_uri is heap-allocated and is NULL when the target is in the same
 * document as the source.  For cross-file references it holds the URI of
 * the file that defines the target symbol.
 */
typedef struct {
    LspRange  source;     /* range of the reference expression */
    LspRange  target;     /* selection_range of the target symbol */
    char     *target_uri; /* heap-allocated; NULL means same document */
} DefinitionLink;

/* ── ParseResult ─────────────────────────────────────────────────────────── */

typedef struct {
    Diagnostic     *diagnostics;
    int             num_diagnostics;
    int             diag_cap;
    int             dep_diag_start; /* diagnostics[0..dep_diag_start-1] are syntax errors */
    DocSymbol      *doc_symbols;
    int             num_doc_symbols;
    int             doc_sym_cap;
    TokenSpan      *tok_spans;
    int             num_tok_spans;
    int             tok_span_cap;
    DefinitionLink *def_links;
    int             num_def_links;
    int             def_link_cap;
    DepRef         *raw_dep_refs;     /* captured dep refs for cross-file revalidation */
    int             num_raw_dep_refs;
    int             raw_dep_ref_cap;
} ParseResult;

/* ── Public API ──────────────────────────────────────────────────────────── */

ParseResult parse(const char *src);
void        parse_result_free(ParseResult *r);
void        doc_symbol_free(DocSymbol *s);

/*
 * Navigate the DocSymbol tree by following `path` (an array of `plen`
 * identifier strings).  Returns the children array of the matching node and
 * sets *out_n to its length, or NULL / *out_n=0 if the path is not found.
 * Only SK_FUNCTION nodes are traversed (i.e. tasks).
 */
const DocSymbol *doc_symbol_find_path(const DocSymbol *syms, int n,
                                      const char **path, int plen,
                                      int *out_n);

/* Compare two positions. Returns -1, 0, or 1. */
static inline int pos_cmp(LspPos a, LspPos b) {
    if (a.line != b.line) return (a.line < b.line) ? -1 : 1;
    if (a.character != b.character) return (a.character < b.character) ? -1 : 1;
    return 0;
}
